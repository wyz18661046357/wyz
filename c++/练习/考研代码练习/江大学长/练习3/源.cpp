/*(2)用 switch 语句编程序*/

#include<iostream>
using namespace std;
int main()
{
	int i;
	cout << "请输入当月利润：";
	cin >> i;
		double n;
		int m = 0;
	if (i <= 100000)
	{
		m=0;
	}
	else if (i > 100000 && i <= 200000)
	{
		m=1;
	}
	else if (i > 200000 && i <= 400000)
	{
		m=2;
	}
	else if (i > 400000 && i <= 600000)
	{
		m=3;
	}
	else if (i > 600000 && i <= 1000000)
	{
		m=4;
	}
	else
	{
		m=5;
	}
	switch(m)
	{
	case 0:	n = i * 0.1;break;
	case 1:n = 100000 * 0.1 + (i - 100000) * 0.075;break;
	case 2:n = 100000 * 0.1 + 100000 * 0.075 + (i - 200000) * 0.05;break;
	case 3:n = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (i - 400000) * 0.03;break;
	case 4:n = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.003 + (i - 600000) * 0.015;break;
	case 5:n = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.003 + 400000 * 0.015 + (i - 1000000) * 0.01;break;
	}
	cout << "应发奖金总数为：" << n;
	return 0;
}

